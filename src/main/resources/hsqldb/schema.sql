DROP TABLE IF EXISTS ZZZ_CRUD;

CREATE TABLE ZZZ_CRUD (
    CRUD_ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    USER_NM VARCHAR(10),
    USER_EMAIL VARCHAR(50),
    USER_AGE INTEGER,
    USER_BIRTH DATE,
    USER_GENDER VARCHAR(1) DEFAULT 'X' NOT NULL,
    INSERT_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATE_DATE TIMESTAMP
);